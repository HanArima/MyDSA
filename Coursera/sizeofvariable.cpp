#include <iostream>
using namespace std;

int main(){
    int a = 10;
    cout << "Size of int: " << sizeof(a) << endl;

    float b;
    cout << "Size of float: " << sizeof(b) << endl;

    double d;
    cout << "Size of double: " << sizeof(d) << endl;

    bool q;
    cout << "Size of bool: " << sizeof(q) << endl;

}